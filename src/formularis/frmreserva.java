    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if (txtidroom.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "You must select a room");
            txtidroom.requestFocus();
            return;
        }
        if (txtidclient.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "You must select a client");
            txtidclient.requestFocus();
            return;
        }

        if (txtprice.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "You must enter a price of the Accommodation");
            txtprice.requestFocus();
            return;
        }

        reservation res = new reservation();
        logReservation func = new logReservation(in, out);
        
        res.setIdRoom(Integer.parseInt(txtidroom.getText()));
        res.setIdClient(Integer.parseInt(txtidclient.getText()));
        res.setIdEmployed(iduser);
        
        int seleccion = cbotipo_reserve.getSelectedIndex();
        res.setTypeReserve((String) cbotipo_reserve.getItemAt(seleccion));
        
        Calendar cal;
        int d,m,a;
        cal=jDateReservation.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR) - 1900;
        res.setDateReservation(new Date(a,m,d));
        
        cal=jDateAdmission.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR) - 1900;
        res.setDateAdmission(new Date(a,m,d));
        
        cal=jDateDeparture.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR) - 1900;
        res.setDateDeparture(new Date(a,m,d));
        
        res.setPryce(Double.parseDouble(txtprice.getText()));
        seleccion = cboestate.getSelectedIndex();
        res.setState((String) cboestate.getItemAt(seleccion));
        
        
        if (accion.equals("save")) {
            if (func.insertar(res)) {
                JOptionPane.showMessageDialog(rootPane, "The reservation was successfully registered");
                mostrar();
                inhabilitar();
                
                // We occupy the rented room
                logRooms funcRoom = new logRooms(in, out);
                room room = new room();
                
                room.setIdroom(Integer.parseInt(txtidroom.getText()));
                funcRoom.occupied(room);
            }

        }
        else if (accion.equals("edit")){
            res.setIdReserve(Integer.parseInt(txtidreserva.getText()));
            res.setIdEmployed(Integer.parseInt(txtidemployed.getText()));            
            if (func.editar(res)) {
                JOptionPane.showMessageDialog(rootPane, "The reservation was Edited satisfactorily");
                mostrar();
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

