    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtidreserva.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Are you sure to Delete the Reservation?","Confirm",2);

            if (confirmacion==0) {
                logReservation func = new logReservation(in, out);
                reservation res = new reservation();

                res.setIdReserve(Integer.parseInt(txtidreserva.getText()));
                func.eliminar(res);
                mostrar();
                inhabilitar();

            }

        }
    }//GEN-LAST:event_btneliminarActionPerformed

